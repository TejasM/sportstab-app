package com.example.coachingtab;

public class EditFragment {
	private float start_percent;
	private float end_percent;
	private int type;
	
	public EditFragment(float s, float d, int t){
		this.start_percent = s;
		this.end_percent = d;
		this.type = t;
	}
	public void setStart(float s){
		this.start_percent = s;
	}
	public float getStart(){
		return this.start_percent;
	}
	public void setEnd(float d){
		this.end_percent = d;
	}
	public float getEnd(){
		return this.end_percent;
	}
	public void setType(int t){
		this.type = t;
	}
	public int getType(){
		return this.type;
	}
	public float getDuration(){
		return this.end_percent-this.start_percent;
	}
}
